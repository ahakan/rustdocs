fn main() {
    let x = 5;

    let mut y = 10;     // mutable variable

    println!("Hello, world! x: {}", x);

    // x = x + 5;       // immutable
    y = y + 5;

    println!("y: {}", y);

    let z: u64 = 10;    // i->signed integer u->unsigned integer

    println!("unsigned64 z: {}", z);

}
